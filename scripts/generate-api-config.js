const fs = require('fs');
const path = require('path');

const apiBase = process.env.API_BASE || process.env.VERCEL_API_BASE || '';
const out = `// generated by scripts/generate-api-config.js - do not edit
window.__API_BASE = ${JSON.stringify(apiBase)};
`;

const outPath = path.join(__dirname, '..', 'js', 'api-config.js');
try{
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, out, 'utf8');
  console.log('Wrote', outPath);
}catch(e){
  console.error('Failed to write api-config.js', e);
  process.exit(1);
}
